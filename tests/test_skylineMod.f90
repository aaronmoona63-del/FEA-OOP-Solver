program test_skylineMod
  use Types
  use MatrixSkyline
  implicit none

  type(SkylineMatrix) :: K
  integer :: n

  n = 5

  !------------------------------------------
  ! 初始化 skyline：nnz = 7（上三角总个数）
  !------------------------------------------
  call K%init(n, n, 7)

  !------------------------------------------
  ! 手动设置 jpoin（必须长度为 n）
  !
  ! 对应矩阵：
  !   | K11  K12  K13    0    0 |
  !   | K21  K22  K23   K24   0 |
  !   | K31  K32  K33   K34  K35|
  !   |  0   K42  K43   K44  K45|
  !   |  0    0   K53   K54  K55|
  !
  ! AUPP = [K12, K13, K23, K24, K34, K35, K45]
  ! ALOW = [K21, K31, K32, K42, K43, K53, K54]
  !------------------------------------------
  K%jpoin = [0, 1, 3, 5, 7]

  !------------------------------------------
  ! 填数据（必须与 FEAP 例子一致）
  !------------------------------------------
  K%diag = [4.0, 5.0, 6.0, 7.0, 8.0]

  K%aupp = [  &
      1.0, 2.0,      & ! K12, K13
      1.5, 0.5,      & ! K23, K24
      3.0, 1.0,      & ! K34, K35
      0.2            & ! K45
  ]

  K%alow = [  &
      1.0, 2.0,      & ! K21, K31
      1.5,                & ! K32
      0.5,                & ! K42
      3.0, 1.0      & ! K43, K53, K54
  ]

  ! 是对称矩阵吗？这里我们假设不是（完整 L 和 U）
  K%unsymmetric = .true.

  !------------------------------------------
  ! 打印 dense 矩阵
  !------------------------------------------
  call K%print()

end program test_skylineMod
